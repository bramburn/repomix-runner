import * as vscode from 'vscode';
import * as fs from 'fs';
import * as path from 'path';
import { BundleManager } from '../core/bundles/bundleManager.js';
import { runBundle } from '../commands/runBundle.js';
import { runRepomix, defaultRunRepomixDeps } from '../commands/runRepomix.js';
import { resolveBundleOutputPath } from '../core/files/outputPathResolver.js';
import { calculateBundleStats, invalidateStatsCache } from '../core/files/fileStats.js';
import { getCwd } from '../config/getCwd.js';
import { WebviewBundle } from '../core/bundles/types.js';
import { copyToClipboard } from '../core/files/copyToClipboard.js';
import { getWorkspaceFiles } from '../agent/tools.js';
import { tempDirManager } from '../core/files/tempDirManager.js';
import { mergeConfigs, readRepomixFileConfig, readRepomixRunnerVscodeConfig } from '../config/configLoader.js';
import { WebviewMessageSchema } from './messageSchemas.js';
import { DatabaseService } from '../core/storage/databaseService.js';

const DEFAULT_REPOMIX_ID = '__default__';

export interface QueueItem {
  executionId: string;
  bundleId: string;
  compress?: boolean;
}

export class RepomixWebviewProvider implements vscode.WebviewViewProvider {
  public static readonly viewType = 'repomixRunner.controlPanel';
  private _view?: vscode.WebviewView;
  private _executionQueue: QueueItem[] = [];
  private _isProcessingQueue = false;
  private _runningBundles: Map<string, AbortController> = new Map();
  // Map bundleId -> { watcher, currentPath } to track active watchers smarty
  private _outputFileWatchers: Map<string, { watcher: vscode.FileSystemWatcher, path: string }> = new Map();
  private _defaultRepomixWatcher?: vscode.FileSystemWatcher;
  private _lastWatchedRepomixOutputPath?: string;
  private _debounceTimer?: NodeJS.Timeout;
  private _bundlesDebounceTimer?: NodeJS.Timeout;

  constructor(
    private readonly _extensionUri: vscode.Uri,
    private readonly _bundleManager: BundleManager,
    private readonly _context: vscode.ExtensionContext,
    private readonly _databaseService: DatabaseService
  ) {}

  public resolveWebviewView(
    webviewView: vscode.WebviewView,
    context: vscode.WebviewViewResolveContext,
    _token: vscode.CancellationToken
  ) {
    this._view = webviewView;

    webviewView.webview.options = {
      enableScripts: true,
      localResourceRoots: [vscode.Uri.joinPath(this._extensionUri, 'dist')],
    };

    webviewView.webview.html = this._getHtmlForWebview(webviewView.webview);

    webviewView.webview.onDidReceiveMessage(async (data) => {
      let message;
      try {
        message = WebviewMessageSchema.parse(data);
      } catch (error) {
        console.error('Invalid webview message:', error);
        return;
      }

      switch (message.command) {
        case 'webviewLoaded': {
          await this._sendBundles();
          await this._sendDefaultRepomixState();
          await this._sendVersion();
          break;
        }
        case 'runBundle': {
          const { bundleId, compress } = message;
          await this._handleRunBundle(bundleId, compress);
          break;
        }
        case 'cancelBundle': {
          const { bundleId } = message;
          await this._handleCancelBundle(bundleId);
          break;
        }
        case 'copyBundleOutput': {
          const { bundleId } = message;
          await this._handleCopyBundleOutput(bundleId);
          break;
        }
        case 'runDefaultRepomix': {
          const { compress } = message;
          await this._handleRunDefaultRepomix(compress);
          break;
        }
        case 'cancelDefaultRepomix': {
          await this._handleCancelDefaultRepomix();
          break;
        }
        case 'copyDefaultRepomixOutput': {
          await this._handleCopyDefaultRepomixOutput();
          break;
        }
        case 'checkApiKey': {
          const key = await this._context.secrets.get('repomix.agent.googleApiKey');
          this._view?.webview.postMessage({
            command: 'apiKeyStatus',
            hasKey: !!key
          });
          break;
        }
        case 'saveApiKey': {
          const { apiKey } = message;
          await this._handleSaveApiKey(apiKey);
          break;
        }
        case 'runSmartAgent': {
          const { query, queryId } = message;
          await this._handleRunSmartAgent(query, queryId);
          break;
        }
        case 'getAgentHistory': {
          await this._handleGetAgentHistory();
          break;
        }
        case 'rerunAgent': {
          const { runId, useSavedFiles } = message;
          await this._handleRerunAgent(runId, useSavedFiles);
          break;
        }
        case 'regenerateAgentRun': {
          const { runId } = message;
          await vscode.commands.executeCommand('repomixRunner.regenerateAgentRun', runId);
          break;
        }
        case 'copyAgentOutput': {
          const { runId } = message;
          await this._handleCopyAgentOutput(runId);
          break;
        }
        case 'copyLastAgentOutput': {
          const { outputPath } = message;
          await this._handleCopyLastAgentOutput(outputPath);
          break;
        }
        case 'openFile': {
          const { path } = message;
          await this._handleOpenFile(path);
          break;
        }
        case 'getSavedQueries': {
          await this._handleGetSavedQueries();
          break;
        }
        case 'deleteQuery': {
          const { queryId } = message;
          await this._handleDeleteQuery(queryId);
          break;
        }
      }
    });

    // Listen for bundle changes
    const changeSubscription = this._bundleManager.onDidChangeBundles.event(() => {
      invalidateStatsCache(); // Invalidate stats when bundles change
      if (this._view?.visible) {
        this._debouncedSendBundles();
      }
    });

    // Listen for window focus to re-check file existence
    const focusSubscription = vscode.window.onDidChangeWindowState((e) => {
       if (e.focused && this._view?.visible) {
         this._debouncedSendBundles();
         this._sendDefaultRepomixState();
       }
    });

    // Clean up subscription when webview is disposed
    webviewView.onDidDispose(() => {
      changeSubscription.dispose();
      focusSubscription.dispose();
      this._disposeWatchers();
    });

    // Initial state send
    if (this._view?.visible) {
        this._sendDefaultRepomixState();
    }
  }

  private _disposeWatchers() {
      this._outputFileWatchers.forEach(item => item.watcher.dispose());
      this._outputFileWatchers.clear();
      this._defaultRepomixWatcher?.dispose();
      if (this._debounceTimer) {
          clearTimeout(this._debounceTimer);
      }
      if (this._bundlesDebounceTimer) {
          clearTimeout(this._bundlesDebounceTimer);
      }
  }

  private async _resolveDefaultRepomixOutputPath(): Promise<string> {
      const cwd = getCwd();
      const vscodeConfig = readRepomixRunnerVscodeConfig();
      const configFile = await readRepomixFileConfig(cwd);
      const mergedConfig = await mergeConfigs(cwd, configFile, vscodeConfig, null);
      return mergedConfig.output.filePath;
  }

  private _debouncedSendDefaultRepomixState() {
      if (this._debounceTimer) {
          clearTimeout(this._debounceTimer);
      }
      this._debounceTimer = setTimeout(() => {
          this._sendDefaultRepomixState();
      }, 500); // Debounce for 500ms
  }

  private async _sendDefaultRepomixState() {
      if (!this._view) {
          return;
      }
      try {
          const outputPath = await this._resolveDefaultRepomixOutputPath();
          const exists = fs.existsSync(outputPath);

          // Update watcher only if path changed
          if (outputPath !== this._lastWatchedRepomixOutputPath) {
              if (this._defaultRepomixWatcher) {
                 this._defaultRepomixWatcher.dispose();
              }

              const watcher = vscode.workspace.createFileSystemWatcher(new vscode.RelativePattern(path.dirname(outputPath), path.basename(outputPath)));
              watcher.onDidCreate(() => this._debouncedSendDefaultRepomixState());
              watcher.onDidDelete(() => this._debouncedSendDefaultRepomixState());
              this._defaultRepomixWatcher = watcher;
              this._lastWatchedRepomixOutputPath = outputPath;
          }

          this._view.webview.postMessage({
              command: 'updateDefaultRepomix',
              data: {
                  outputFileExists: exists,
                  outputFilePath: outputPath
              }
          });

      } catch (e) {
          console.error('Failed to send default repomix state:', e);
      }
  }

  private _debouncedSendBundles() {
    if (this._bundlesDebounceTimer) {
        clearTimeout(this._bundlesDebounceTimer);
    }
    this._bundlesDebounceTimer = setTimeout(() => {
        this._sendBundles();
    }, 300);
  }

  private async _sendBundles() {
    if (!this._view) {
      return;
    }
    const bundleMetadata = await this._bundleManager.getAllBundles();
    const cwd = getCwd();
    const activeBundleIds = new Set(Object.keys(bundleMetadata.bundles));

    // Cleanup watchers for removed bundles
    for (const [id, item] of this._outputFileWatchers) {
        if (!activeBundleIds.has(id)) {
            item.watcher.dispose();
            this._outputFileWatchers.delete(id);
        }
    }

    const webviewBundles: WebviewBundle[] = await Promise.all(
        Object.entries(bundleMetadata.bundles).map(async ([id, bundle]) => {
            const outputPath = await resolveBundleOutputPath(bundle);
            const exists = fs.existsSync(outputPath);

            // Smart Watcher Management:
            // Only recreate watcher if it doesn't exist OR the path has changed
            const existingWatcher = this._outputFileWatchers.get(id);

            if (!existingWatcher || existingWatcher.path !== outputPath) {
                // Dispose old if exists (path changed)
                if (existingWatcher) {
                    existingWatcher.watcher.dispose();
                }

                // Create new watcher
                const watcher = vscode.workspace.createFileSystemWatcher(
                    new vscode.RelativePattern(path.dirname(outputPath), path.basename(outputPath))
                );

                // Use debounced update to avoid spamming
                watcher.onDidCreate(() => this._debouncedSendBundles());
                watcher.onDidDelete(() => this._debouncedSendBundles());
                
                this._outputFileWatchers.set(id, { watcher, path: outputPath });
            }

            // Calculate stats
            const stats = await calculateBundleStats(cwd, id, bundle.files);

            return {
                id,
                ...bundle,
                outputFilePath: outputPath,
                outputFileExists: exists,
                stats
            };
        })
    );

    this._view.webview.postMessage({
      command: 'updateBundles',
      bundles: webviewBundles,
    });
  }

  private async _sendVersion() {
    if (!this._view) {
      return;
    }
    try {
      const packageJsonPath = vscode.Uri.joinPath(this._extensionUri, 'package.json');
      const packageJsonData = await vscode.workspace.fs.readFile(packageJsonPath);
      const packageJson = JSON.parse(Buffer.from(packageJsonData).toString());
      const version = packageJson.version;

      this._view.webview.postMessage({
        command: 'updateVersion',
        version,
      });
    } catch (error) {
      console.error('Failed to get version:', error);
    }
  }

  private async _handleCopyBundleOutput(bundleId: string) {
    const bundle = await this._bundleManager.getBundle(bundleId);
    if (!bundle) {return;}

    try {
        const outputPath = await resolveBundleOutputPath(bundle);
        if (!fs.existsSync(outputPath)) {
            vscode.window.showErrorMessage(`Output file not found: ${outputPath}`);
            return;
        }

        // Use original filename without timestamp prefix
        const originalFilename = path.basename(outputPath);
        const uniqueSubdir = `copy_${Date.now()}`;
        const tmpDir = path.join(tempDirManager.getTempDir(), uniqueSubdir);

        // Ensure subdirectory exists
        await fs.promises.mkdir(tmpDir, { recursive: true });

        const tmpFilePath = path.join(tmpDir, originalFilename);

        await copyToClipboard(outputPath, tmpFilePath);
        vscode.window.showInformationMessage(`Copied "${originalFilename}" to clipboard.`);

        await tempDirManager.cleanupFile(tmpFilePath);
    } catch (err: unknown) {
        const errorMessage = err instanceof Error ? err.message : String(err);
        vscode.window.showErrorMessage(`Failed to copy output: ${errorMessage}`);
    }
  }

  private async _handleCopyDefaultRepomixOutput() {
      try {
          const outputPath = await this._resolveDefaultRepomixOutputPath();
          if (!fs.existsSync(outputPath)) {
              vscode.window.showErrorMessage(`Output file not found: ${outputPath}`);
              return;
          }

          // Use original filename without timestamp prefix
          const originalFilename = path.basename(outputPath);
          const uniqueSubdir = `copy_${Date.now()}`;
          const tmpDir = path.join(tempDirManager.getTempDir(), uniqueSubdir);

          // Ensure subdirectory exists
          await fs.promises.mkdir(tmpDir, { recursive: true });

          const tmpFilePath = path.join(tmpDir, originalFilename);

          await copyToClipboard(outputPath, tmpFilePath);
          vscode.window.showInformationMessage(`Copied "${originalFilename}" to clipboard.`);
          await tempDirManager.cleanupFile(tmpFilePath);

      } catch (err: unknown) {
          const errorMessage = err instanceof Error ? err.message : String(err);
          vscode.window.showErrorMessage(`Failed to copy output: ${errorMessage}`);
      }
  }

  private async _handleRunDefaultRepomix(compress?: boolean) {
      if (!this._view) { return; }

      // Add to queue
      this._executionQueue.push({ bundleId: DEFAULT_REPOMIX_ID, compress, executionId: '' });

      // Notify queued
      this._view.webview.postMessage({
        command: 'executionStateChange',
        bundleId: DEFAULT_REPOMIX_ID,
        status: 'queued'
      });

      vscode.window.showInformationMessage(`Default Repomix run queued${compress ? ' (compressed)' : ''}.`);
      this._processQueue();
  }

  private async _handleCancelDefaultRepomix() {
     await this._handleCancelBundle(DEFAULT_REPOMIX_ID);
  }

  private async _handleRunBundle(bundleId: string, compress?: boolean) {
    if (!this._view) {
      return;
    }

    // Add to queue
    this._executionQueue.push({ bundleId, compress, executionId: '' });

    // Notify queued
    this._view.webview.postMessage({
      command: 'executionStateChange',
      bundleId,
      status: 'queued'
    });

    // Get bundle name for notification
    const bundle = await this._bundleManager.getBundle(bundleId);
    const bundleName = bundle?.name ?? "Unknown Bundle";

    vscode.window.showInformationMessage(`Bundle "${bundleName}" queued${compress ? ' (compressed)' : ''}.`);

    this._processQueue();
  }

  private async _handleCancelBundle(bundleId: string) {
    // Case 1: Bundle is currently running
    const controller = this._runningBundles.get(bundleId);

    if (controller) {
        controller.abort();
        // The _processQueue loop will handle the cleanup and notification via catch/finally
        // But we can notify immediately that cancellation was requested
        let name = "Unknown";
        if (bundleId === DEFAULT_REPOMIX_ID) {
            name = "Default Repomix";
        } else {
            const bundle = await this._bundleManager.getBundle(bundleId);
            name = bundle?.name ?? "Unknown Bundle";
        }
        vscode.window.showInformationMessage(`Cancelling "${name}"...`);
        return;
    }

    // Case 2: Bundle is in the queue (waiting)
    // Find the first occurrence of this bundleId in the queue
    const queueIndex = this._executionQueue.findIndex(item => item.bundleId === bundleId);
    if (queueIndex !== -1) {
      this._executionQueue.splice(queueIndex, 1);
      this._notifyCancel(bundleId);
      return;
    }
  }

  private async _notifyCancel(bundleId: string) {
      if (this._view) {
        this._view.webview.postMessage({
            command: 'executionStateChange',
            bundleId,
            status: 'idle',
        });
      }

      let name = "Unknown";
      if (bundleId === DEFAULT_REPOMIX_ID) {
          name = "Default Repomix";
      } else {
          const bundle = await this._bundleManager.getBundle(bundleId);
          name = bundle?.name ?? "Unknown Bundle";
      }
      vscode.window.showInformationMessage(`"${name}" removed from queue.`);
  }

  private async _processQueue() {
    if (this._isProcessingQueue) {
      return;
    }

    this._isProcessingQueue = true;

    while (this._executionQueue.length > 0) {
      const queueItem = this._executionQueue[0];
      const { bundleId, compress } = queueItem;

      if (!this._view) {
        break;
      }

      // Notify running
      this._view.webview.postMessage({
        command: 'executionStateChange',
        bundleId,
        status: 'running'
      });

      const isDefault = bundleId === DEFAULT_REPOMIX_ID;
      let bundleName = "Default Repomix";
      if (!isDefault) {
          const bundle = await this._bundleManager.getBundle(bundleId);
          bundleName = bundle ? `Bundle "${bundle.name}"` : "Unknown Bundle";
      }

      vscode.window.showInformationMessage(`Starting ${bundleName}${compress ? ' (compressed)' : ''}...`);

      const controller = new AbortController();
      this._runningBundles.set(bundleId, controller);

      try {
        if (isDefault) {
            await runRepomix({
              ...defaultRunRepomixDeps,
              mergeConfigOverride: compress ? { output: { compress: true } } : null,
              signal: controller.signal,
            });
            this._sendDefaultRepomixState();
        } else {
            // Need to update runBundle signature to accept overrides or compress flag
            const overrides = compress ? { output: { compress: true } } : undefined;
            await runBundle(this._bundleManager, bundleId, controller.signal, overrides);
            // Refresh bundles to update "exists" status for the newly generated file
            this._sendBundles();
        }

        vscode.window.showInformationMessage(`${bundleName} completed successfully.`);
      } catch (error: unknown) {
        const errorMessage = error instanceof Error ? error.message : String(error);
        if (errorMessage === 'Aborted' || (error instanceof Error && error.name === 'AbortError')) {
            vscode.window.showInformationMessage(`${bundleName} was cancelled.`);
        } else {
            console.error('Error running from webview:', error);
            vscode.window.showErrorMessage(`Failed to run: ${errorMessage}`);
        }
      } finally {
        // Cleanup
        this._runningBundles.delete(bundleId);

        // Remove from queue - ONLY if it's still the head AND matches bundleId (robust check)
        if (this._executionQueue.length > 0 && this._executionQueue[0].bundleId === bundleId) {
             this._executionQueue.shift();
        }

        // Notify idle
        if (this._view) {
          this._view.webview.postMessage({
            command: 'executionStateChange',
            bundleId,
            status: 'idle'
          });
        }
      }
    }

    this._isProcessingQueue = false;
  }

  private async _handleCheckApiKey(): Promise<boolean> {
    const apiKey = await this._context.secrets.get('repomix.agent.googleApiKey');
    return !!apiKey;
  }

  private async _handleSaveApiKey(apiKey: string) {
    if (apiKey) {
      await this._context.secrets.store('repomix.agent.googleApiKey', apiKey);
      vscode.window.showInformationMessage('API Key saved successfully!');
    }
  }

  private async _handleRunSmartAgent(query: string, queryId?: string) {
    const workspaceRoot = getCwd();

    // Check for API key first
    let apiKey = await this._context.secrets.get('repomix.agent.googleApiKey');
    if (!apiKey) {
      // Fallback to config
      apiKey = vscode.workspace.getConfiguration('repomix.agent').get<string>('googleApiKey');
    }

    if (!apiKey) {
      vscode.window.showErrorMessage("Google API Key missing. Please set it in the 'Smart Agent' tab.");
      this._view?.webview.postMessage({ command: 'agentRunFailed' });
      return;
    }

    // Notify webview that agent is running
    if (this._view) {
      this._view.webview.postMessage({ command: 'agentStateChange', status: 'running' });
    }

    // Wrap execution in withProgress to show visible progress in VS Code
    await vscode.window.withProgress({
      location: vscode.ProgressLocation.Notification,
      title: "Repomix Agent",
      cancellable: true
    }, async (progress, token) => {
      try {
        progress.report({ message: "Initializing...", increment: 0 });

        const { createSmartRepomixGraph } = await import('../agent/graph.js');
        const app = createSmartRepomixGraph(this._databaseService);

        const inputs = {
          apiKey: apiKey,
          userQuery: query,
          workspaceRoot: workspaceRoot,
          allFilePaths: [],
          candidateFiles: [],
          confirmedFiles: [],
          finalCommand: "",
          queryId: queryId,
          outputPath: undefined
        };

        const config = { configurable: { thread_id: `agent_${Date.now()}` } };

        // We use stream() instead of invoke() to get updates from the graph nodes
        // We accumulate the state manually to have the 'finalState' at the end
        let finalState: any = { ...inputs };
        const stream = await app.stream(inputs, config);

        for await (const chunk of stream) {
          if (token.isCancellationRequested) {
            throw new Error("Operation cancelled by user");
          }

          // The chunk object keys are the node names (e.g., { filtering: { ... } })
          const nodeName = Object.keys(chunk)[0];
          const update = (chunk as any)[nodeName];

          // Merge updates into final state
          finalState = { ...finalState, ...update };

          // Extract current total tokens
          const currentTokens = finalState.totalTokens || 0;

          // Update the VS Code notification based on the active node
          switch (nodeName) {
            case 'indexing':
              progress.report({ message: "Scanning workspace files...", increment: 10 });
              break;
            case 'structureExtraction':
              progress.report({ message: "Analyzing project structure...", increment: 10 });
              break;
            case 'filtering':
              const candidates = update.candidateFiles?.length || 0;
              progress.report({ message: `Filtering: found ${candidates} potential files...`, increment: 20 });
              break;
            case 'relevanceCheck':
              const confirmed = update.confirmedFiles?.length || 0;
              progress.report({
                message: `Deep analysis: confirmed ${confirmed} relevant files (${currentTokens.toLocaleString()} tokens)...`,
                increment: 30
              });
              break;
            case 'commandGeneration':
              progress.report({ message: "Generating Repomix command...", increment: 10 });
              break;
            case 'execution':
              progress.report({ message: "Running Repomix CLI...", increment: 20 });
              break;
          }
        }

        const fileCount = finalState.confirmedFiles.length;
        const outputPath = finalState.outputPath;
        const totalTokens = finalState.totalTokens || 0;

        if (fileCount > 0 && outputPath) {
          // Success - notify webview with output path
          this._view?.webview.postMessage({
            command: 'agentRunComplete',
            outputPath: outputPath,
            fileCount: fileCount,
            query: query,
            tokens: totalTokens
          });

          vscode.window.showInformationMessage(`Agent successfully packaged ${fileCount} files! (Used ${totalTokens.toLocaleString()} tokens)`);
        } else {
          // No files found
          this._view?.webview.postMessage({ command: 'agentRunFailed' });
          vscode.window.showWarningMessage(`No relevant files found for: "${query}"`);
        }
      } catch (error: any) {
        this._view?.webview.postMessage({ command: 'agentRunFailed' });

        const errorMessage = error instanceof Error ? error.message : String(error);

        // Specific error handling for missing API key
        if (errorMessage.includes('Google API Key')) {
          const selection = await vscode.window.showErrorMessage(
            'Google API Key missing.',
            'Open Settings'
          );
          if (selection === 'Open Settings') {
            vscode.commands.executeCommand(
              'workbench.action.openSettings',
              'repomix.agent.googleApiKey'
            );
          }
        } else if (errorMessage.includes('cancelled')) {
           vscode.window.showInformationMessage("Agent run cancelled.");
        } else {
          vscode.window.showErrorMessage(`Agent failed: ${errorMessage}`);
        }
      } finally {
        // Notify webview that agent is done
        if (this._view) {
          this._view.webview.postMessage({ command: 'agentStateChange', status: 'idle' });
        }
      }
    });
  }

  private async _handleGetAgentHistory(): Promise<void> {
    if (!this._view) {
      return;
    }

    try {
      const history = await this._databaseService.getAgentRunHistory(50);
      this._view.webview.postMessage({
        command: 'agentHistory',
        history
      });
    } catch (error: any) {
      console.error('Failed to get agent history:', error);
      vscode.window.showErrorMessage(`Failed to get agent history: ${error.message}`);
    }
  }

  private async _handleRerunAgent(runId: string, useSavedFiles: boolean): Promise<void> {
    if (!this._view) {
      return;
    }

    try {
      // Get the previous run from database
      const previousRun = await this._databaseService.getAgentRunById(runId);
      if (!previousRun) {
        vscode.window.showErrorMessage('Previous agent run not found');
        return;
      }

      if (useSavedFiles && previousRun.files.length === 0) {
        vscode.window.showWarningMessage('No saved file list for this run');
        return;
      }

      // Notify webview
      this._view.webview.postMessage({
        command: 'agentStateChange',
        status: 'running'
      });

      const createSmartRepomixGraph = (await import('../agent/graph.js')).createSmartRepomixGraph;
      const app = createSmartRepomixGraph(this._databaseService);

      // Check for API key first
      let apiKey = await this._context.secrets.get('repomix.agent.googleApiKey');
      if (!apiKey) {
        // Fallback to config
        apiKey = vscode.workspace.getConfiguration('repomix.agent').get<string>('googleApiKey');
      }

      if (!apiKey) {
        vscode.window.showErrorMessage("Google API Key missing. Please set it in the 'Smart Agent' tab.");
        return;
      }

      const inputs = {
        apiKey,
        userQuery: previousRun.query,
        workspaceRoot: getCwd(),
        allFilePaths: useSavedFiles ? await getWorkspaceFiles(getCwd()) : [],
        candidateFiles: [],
        confirmedFiles: useSavedFiles ? previousRun.files : [], // Use saved files or empty for fresh scan
        finalCommand: '',
        queryId: previousRun.queryId,
        outputPath: previousRun.outputPath
      };

      const config = { configurable: { thread_id: `rerun_${Date.now()}` } };

      // Skip to appropriate node based on whether we're re-running or re-packing
      if (useSavedFiles) {
        // Skip directly to command generation since we already have the file list
        inputs.allFilePaths = await getWorkspaceFiles(inputs.workspaceRoot);
        const finalState = await app.invoke(inputs, config);
        vscode.window.showInformationMessage(`Re-packed ${finalState.confirmedFiles.length} files from saved list`);
      } else {
        // Full fresh scan
        const finalState = await app.invoke(inputs, config);
        vscode.window.showInformationMessage(`Fresh scan completed. Found ${finalState.confirmedFiles.length} files`);
      }

    } catch (error) {
      vscode.window.showErrorMessage(`Re-run failed: ${error}`);
    } finally {
      this._view.webview.postMessage({
        command: 'agentStateChange',
        status: 'idle'
      });
    }
  }

  private async _handleCopyAgentOutput(runId: string): Promise<void> {
    try {
      const run = await this._databaseService.getAgentRunById(runId);
      if (!run?.outputPath || !fs.existsSync(run.outputPath)) {
        vscode.window.showErrorMessage('Output file not found');
        return;
      }

      const originalFilename = path.basename(run.outputPath);
      const uniqueSubdir = `copy_${Date.now()}`;
      const tmpDir = path.join(tempDirManager.getTempDir(), uniqueSubdir);

      // Ensure subdirectory exists
      await fs.promises.mkdir(tmpDir, { recursive: true });

      const tmpFilePath = path.join(tmpDir, originalFilename);

      await copyToClipboard(run.outputPath, tmpFilePath);
      vscode.window.showInformationMessage(`Copied ${originalFilename} to clipboard`);
      await tempDirManager.cleanupFile(tmpFilePath);

    } catch (error) {
      vscode.window.showErrorMessage(`Failed to copy output: ${error}`);
    }
  }

  private async _handleCopyLastAgentOutput(outputPath: string): Promise<void> {
    try {
      if (!fs.existsSync(outputPath)) {
        vscode.window.showErrorMessage(`Output file not found: ${outputPath}`);
        return;
      }

      const originalFilename = path.basename(outputPath);
      const uniqueSubdir = `copy_${Date.now()}`;
      const tmpDir = path.join(tempDirManager.getTempDir(), uniqueSubdir);

      // Ensure subdirectory exists
      await fs.promises.mkdir(tmpDir, { recursive: true });

      const tmpFilePath = path.join(tmpDir, originalFilename);

      await copyToClipboard(outputPath, tmpFilePath);
      vscode.window.showInformationMessage(`Copied ${originalFilename} to clipboard`);
      await tempDirManager.cleanupFile(tmpFilePath);

    } catch (error: unknown) {
      const errorMessage = error instanceof Error ? error.message : String(error);
      vscode.window.showErrorMessage(`Failed to copy output: ${errorMessage}`);
    }
  }

  private async _handleOpenFile(filePath: string): Promise<void> {
    try {
      if (!fs.existsSync(filePath)) {
        vscode.window.showErrorMessage(`File not found: ${filePath}`);
        return;
      }

      const uri = vscode.Uri.file(filePath);
      await vscode.commands.executeCommand('vscode.open', uri);
    } catch (error: any) {
      vscode.window.showErrorMessage(`Failed to open file: ${error.message}`);
    }
  }

  private async _handleGetSavedQueries(): Promise<void> {
    if (!this._view) {
      return;
    }

    try {
      const queries = await this._databaseService.getSavedQueries(20);
      this._view.webview.postMessage({
        command: 'savedQueries',
        queries
      });
    } catch (error: any) {
      console.error('Failed to get saved queries:', error);
      vscode.window.showErrorMessage(`Failed to get saved queries: ${error.message}`);
    }
  }

  private async _handleDeleteQuery(queryId: string): Promise<void> {
    try {
      await this._databaseService.deleteQuery(queryId);
      await this._handleGetSavedQueries(); // Refresh the list
      vscode.window.showInformationMessage('Query deleted successfully');
    } catch (error: any) {
      console.error('Failed to delete query:', error);
      vscode.window.showErrorMessage(`Failed to delete query: ${error.message}`);
    }
  }

  private _getHtmlForWebview(webview: vscode.Webview) {
    const scriptUri = webview.asWebviewUri(
      vscode.Uri.joinPath(this._extensionUri, 'dist', 'webview.js')
    );
    const nonce = getNonce();

    return `<!DOCTYPE html>
      <html lang="en">
      <head>
        <meta charset="UTF-8">
        <meta http-equiv="Content-Security-Policy" content="default-src 'none'; style-src ${webview.cspSource} 'unsafe-inline'; script-src 'nonce-${nonce}';">
        <meta name="viewport" content="width=device-width, initial-scale=1.0">
        <title>Repomix Runner Control Panel</title>
      </head>
      <body>
        <div id="root"></div>
        <script nonce="${nonce}" type="module" src="${scriptUri}"></script>
      </body>
      </html>`;
  }
}

function getNonce() {
  let text = '';
  const possible = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
  for (let i = 0; i < 32; i++) {
    text += possible.charAt(Math.floor(Math.random() * possible.length));
  }
  return text;
}